'''
4. Suponha que você tem a seguinte lista de strings em Python: 
frutas = ['laranja', 'banana', 'abacaxi', 'maçã', 'uva', 'morango', 'manga', 'kiwi', 'pera', 'melancia'].
Ordene esta lista em ordem alfabética decrescente e mostre a lista resultante.
'''

frutas = ['laranja', 'banana', 'abacaxi', 'maçã', 'uva', 'morango', 'manga', 'kiwi', 'pera', 'melancia']
frutas.sort(reverse = True)

print(f'\nOrdem decrescente: {frutas}\n')
